//! Shift() method - removes the first element in an array

let arr = [1, 2, 3, 4, 6, 7, 8];

console.log(arr.shift(1)); //1
console.log(arr); // [2,3,4,5,6,7,8]

//! unshift() method - add element in the begining of an array

let arr1 = [1, 2, 3];

console.log(arr1.unshift(4)); //4
console.log(arr1); // [4,1,2,3]
